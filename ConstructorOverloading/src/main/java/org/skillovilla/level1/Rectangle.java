package org.skillovilla.level1;


public class Rectangle {
    int width;
    int height;

    // Uncomment the default constructor


    public Rectangle() {
        this.width = 0;
        this.height = 0;
    }



    // Uncomment the parameterized constructor


    public Rectangle(int width, int height) {
        this.width = width;
        this.height = height;
    }


}
