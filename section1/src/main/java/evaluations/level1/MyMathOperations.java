package evaluations.level1;

public class MyMathOperations {

    public int add(int a, int b) {
        int c = a + b;
        return c;
    }

    public int divide(int a, int b) {
        int c = a/b;
        return c;
    }

    public int multiply(int a, int b) {
        int c = a*b;
        return c;
    }



}
